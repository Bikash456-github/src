
package Assignment;

import java.util.Scanner;

public class Assignment56_Switch_case {
//WAP for swith case having keywords swith,case,default and break 
public static void main(String[] args) {
	Scanner s1=new Scanner(System.in);
	System.out.println("Enter name");
	String name =s1.next();
	switch(name)
	{
	case "Bikash":
		System.out.println("Bikash typed");
		break;
	case "Nita":
		System.out.println("Sarmila typed");
		break;
	case"Sarmila":
		System.out.println("Sarmila typed");
		break;
		default:
			System.out.println("enter Correct Name");
		
	}
	
	
	
	
	
}
	
	
	
	
	
	
	
	
	
}
